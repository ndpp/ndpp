! center-of-mass frame to the laboratory frame.  This produces a matrix (jacob)
! with order_CM columns and order_L rows.
!===============================================================================    
    
    pure subroutine calc_jacobian_legendre(R, order_L, order_CM, j)
      real(8), intent(in)  :: R          ! Reduced Mass
      integer, intent(in)  :: order_L    ! Laboratory Legendre order (rows)
      integer, intent(in)  :: order_CM   ! CM Legendre order (cols)
      real(8), intent(out) :: j(:,:)     ! The resultant jacobian dense matrix
      
      if (R < ONE) then
        call jacob_leg_H(R, order_L, order_CM, j)
        j = ZERO
      else
        call jacob_leg_notH(R, order_L, order_CM, j)
      end if
      
